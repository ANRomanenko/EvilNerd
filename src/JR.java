public class JR {
    public static void main(String[] args) {
        int x = 2;
        int y = 12;

        //x = x * 3;
        y = 2 + 12;                     // у = х + у     у принимает новое значение = 14
        x = 14 - 2;                     // х = у - х.    X принимает новое значение = 12 выводится на печать
        y = 14 - 12;                    // у = у - х.    Y принимает новое значение = 2 выводится на печать

        System.out.println(x);
        System.out.println(y);
    }
}
